panjang = int(input("Masukkan panjang persegi: ")) #input variabel panjang
lebar = int(input("Masukkan lebar persegi: ")) #input variabel lebar
keliling = 2 * (panjang + lebar) #menghitung keliling

print("Keliling permukaan persegi tersebut adalah:", keliling, "CM") #menampilkan keliling

'''
Output: 
Masukkan panjang persegi: 10
Masukkan lebar persegi: 10
Keliling permukaan persegi tersebut adalah: 40 CM'''

