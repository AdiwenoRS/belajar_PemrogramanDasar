panjang = int(input("Masukkan panjang balok: ")) #input variabel panjang
lebar = int(input("Masukkan lebar balok: ")) #input variabel lebar
tinggi = int(input("Masukkan tinggi balok: ")) #input variabel tinggi
volumeBalok = panjang*lebar*tinggi #kalkulasi

print("Keliling permukaan persegi tersebut adalah:", volumeBalok, "CM") #menampilkan volume
